def sieve_of_eratosthenes(limit):
    is_prime = [True] * (limit + 1)
    p = 2
    while (p * p <= limit):
        if (is_prime[p] == True):
            for i in range(p * p, limit + 1, p):
                is_prime[i] = False
        p += 1
    return [p for p in range(2, limit + 1) if is_prime[p]]

def primeCount(n):
    if n < 2:
        return 0
    
    # Generate primes up to a reasonable limit
    primes = sieve_of_eratosthenes(100)
    
    product = 1
    count = 0
    
    for prime in primes:
        if product * prime > n:
            break
        product *= prime
        count += 1
    
    return count

